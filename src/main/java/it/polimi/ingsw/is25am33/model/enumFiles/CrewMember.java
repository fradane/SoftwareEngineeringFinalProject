package it.polimi.ingsw.is25am33.model.enumFiles;

/**
 * Represents the different types of crew members that can be present in the game.
 * These crew members can be assigned to various ship components and may have different
 * abilities or purposes within the gameplay.
 */
public enum CrewMember {
    /**
     * Human crew member.
     */
    HUMAN,

    /**
     * Brown alien crew member.
     */
    BROWN_ALIEN,

    /**
     * Purple alien crew member.
     */
    PURPLE_ALIEN;
}